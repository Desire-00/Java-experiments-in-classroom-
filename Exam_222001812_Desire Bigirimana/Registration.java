    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try{
          Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/login","root","");
          PreparedStatement ps;
          String sql = "INSERT INTO user (ID, NAME, `USER_ NAME`, EMAIL, PHONE, PASSWORD) VALUES (?, ?, ?, ?, ?, ?)";
          ps= con.prepareStatement(sql);
          ps.setString(1,jid.getText() );
          ps.setString(2,jname.getText() );
          ps.setString(3,juser.getText() );
          ps.setString(4,jemail.getText() );
          ps.setString(5,jphone.getText() );
          ps.setString(6,jpass.getText() );
          ps.executeUpdate();
          
          if((jid.getText().equals("")) || (jname.getText().equals("")) || (juser.getText().equals("")) || (jemail.getText().equals("")) || (jphone.getText().equals("")) || (jpass.getText().equals("")) ) {
            JOptionPane.showMessageDialog(null, "ALL FIELD ARE REQUIRED","Message",JOptionPane.ERROR_MESSAGE);
          }else {
          
          JOptionPane.showMessageDialog(null, "success");
           Login lg = new Login();
           lg.setVisible(true);
           setVisible(false);
          
          
          }
       
      
      
      
      }catch(Exception ex){
          System.out.print(ex);
      
      
      }
    }//GEN-LAST:event_jButton1ActionPerformed


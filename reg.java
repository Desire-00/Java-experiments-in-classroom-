    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
      try{
          String query = "INSERT INTO `myshop`(`Fname`, `Lname`, `Addr`, `Gender`, `Email`, `Phone`, `Username`, `Password`) VALUES (? ,? ,? ,? ,? ,? ,? ,?)";
          Con = DriverManager.getConnection("jdbc:mysql://localhost/userregistrationsystem", "root", "");
          pst=Con.prepareStatement(query);
          pst.setString(1, txtFname.getText());
          pst.setString(2, txtLname.getText());
          pst.setString(3, txtAdd.getText());
          pst.setString(4, txtUser.getText());
          pst.setString(5, txtEmail.getText());
          pst.setString(6, cmbGender.getSelectedItem().toString());
          pst.setString(7, txtPhone.getText());
          pst.setString(8, txtPass.getText());
          pst.executeUpdate();
          JOptionPane.showMessageDialog(null, "Register SUCCESSFULLY");
          
      }catch(Exception ex){
          JOptionPane.showMessageDialog(null," not registered");
          
      }
    }//GEN-LAST:event_registerActionPerformed

